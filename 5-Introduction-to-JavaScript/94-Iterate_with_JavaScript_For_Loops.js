// A for loop's code executes a specific number of times. The three parts of a
// for loop declaration are separated by semi-colons. The first part is the
// initialization, the second is the condition, and the third is the final
// expression.

// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

// Setup
var myArray = [];
// Only change code below this line.
