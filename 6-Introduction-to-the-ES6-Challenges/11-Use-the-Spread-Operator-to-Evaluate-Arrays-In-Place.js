// The spread operator (which looks like the rest operator) allows you to
// expand arrays and other expressions where multiple elements are present.

const arr1 = ['JAN', 'FEB', 'MAR', 'APR', 'MAY'];
let arr2;
(function() {
  "use strict";
  arr2 = [...arr1]; // change this line
})();
console.log(arr2);
